public class Date {
    private int day,month,year;

    public void setDate(int year, int month, int day){
        this.year=year;
        this.month=month;
        this.day=day;
    }
    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getMonth() {
        return month;
    }

    public void setMounth(int month) {
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
